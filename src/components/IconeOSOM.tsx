const COLOR_CLASSES = {
  yellow: 'text-yellow-400',
  green: 'text-green-400', 
  purple: 'text-purple-400',
  cyan: 'text-cyan-400',
  pink: 'text-pink-400',
  red: 'text-red-400',
  orange: 'text-orange-400',
  gray: 'text-gray-400',
  white: 'text-white'
}

export default function IconeOSOM({ 
  type, 
  size = 24, 
  className = '', 
  color = 'gray',
  withAnimation = false,
  ariaLabel 
}: IconeOSOMProps) {
  const svg = ICON_SVGS[type]
  const colorClass = COLOR_CLASSES[color]
  
  const baseClasses = [
    colorClass,
    className,
    withAnimation ? 'transition-all duration-300 hover:scale-105 hover:-translate-y-0.5 hover:brightness-110' : ''
  ].filter(Boolean).join(' ')

  if (!svg) {
    console.warn(`IconeOSOM: Type d'icône "${type}" non trouvé`)
    return null
  }

  return (
    <div 
      className={baseClasses}
      style={{ width: size, height: size }}
      role="img"
      aria-label={ariaLabel}
      dangerouslySetInnerHTML={{ __html: svg }}
    />
  )
}